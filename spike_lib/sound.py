from spike_lib.robot import *

class Song:
    def __init__(self, notes: list):
        self.notes = notes

class Sound:
    def __init__(self, hub: PrimeHub):
        self.hub = hub
        self.songs = {}

    def play(self, song: Song, mult=1):
        for i in song.notes:
            self.hub.speaker.beep(i[0], i[1] * mult)
            wait(i[1] * mult)

    def beep(self, frequency=440, duration=100):
        self.hub.speaker.beep(frequency, duration)

